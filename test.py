def calculate_gc_content(dna_string):
    gc_count = dna_string.count('C') + dna_string.count('G')
    gc_content = (gc_count / len(dna_string)) * 100
    return gc_content

def sequence_extractor(s):

    sequences = s.split('>')[1:]


    sequence_dict = {}

    for seq in sequences:
        lines = seq.split('\n')
        label = lines[0]
        sequence = ''.join(lines[1:])
        sequence_dict[label] = sequence

    return sequence_dict

def dict_modifier(ex_dict):
    gc_content_dict = {}
    for key, value in ex_dict.items():
        gc_content = calculate_gc_content(value)
        gc_content_dict[key] = float(gc_content)

    max_key = max(gc_content_dict, key=gc_content_dict.get)
    highest_value = gc_content_dict[max_key]

    return max_key, highest_value

